void main() {
  // 声明整数
  int count = 3;
  print(count);

  // 声明浮点数
  double price = 1.2;
  print(price);

  // 声明数值类型
  num numb = 2.2;
  print(numb);

  // 类型转换
  print(price.toString());
  print(8.9.toInt()); // 向下取整

  // 四舍五入
  print(3.141592653.round());
  print(3.14159265.toStringAsFixed(4));
}